package Data;

/**
 *
 * @author cuhoa_000
 */
public class KhachHang {
    private String MSKH;
    private String HoTenKhach;
    private String DiaChiKhach;
    private String MaSoThue;
    private String TaiKhoan;

    /**
     * @return the MSKH
     */
    public String getMSKH() {
        return MSKH;
    }

    /**
     * @param MSKH the MSKH to set
     */
    public void setMSKH(String MSKH) {
        this.MSKH = MSKH;
    }

    /**
     * @return the HoTenKhach
     */
    public String getHoTenKhach() {
        return HoTenKhach;
    }

    /**
     * @param HoTenKhach the HoTenKhach to set
     */
    public void setHoTenKhach(String HoTenKhach) {
        this.HoTenKhach = HoTenKhach;
    }

    /**
     * @return the DiaChiKhach
     */
    public String getDiaChiKhach() {
        return DiaChiKhach;
    }

    /**
     * @param DiaChiKhach the DiaChiKhach to set
     */
    public void setDiaChiKhach(String DiaChiKhach) {
        this.DiaChiKhach = DiaChiKhach;
    }

    /**
     * @return the MaSoThue
     */
    public String getMaSoThue() {
        return MaSoThue;
    }

    /**
     * @param MaSoThue the MaSoThue to set
     */
    public void setMaSoThue(String MaSoThue) {
        this.MaSoThue = MaSoThue;
    }

    /**
     * @return the TaiKhoan
     */
    public String getTaiKhoan() {
        return TaiKhoan;
    }

    /**
     * @param TaiKhoan the TaiKhoan to set
     */
    public void setTaiKhoan(String TaiKhoan) {
        this.TaiKhoan = TaiKhoan;
    }
    
}
